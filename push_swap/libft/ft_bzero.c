#include "libft.h"

void	*ft_bzero(void *s, size_t len)
{
	char	*str;
	size_t	i;

	str = s;
	i = 0;
	while (len > 0)
	{
		str[i] = 0;
		i++;
		len--;
	}
	return (str);
}
