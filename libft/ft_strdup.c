#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		str_len;
	int		i;
	char	*new_str;

	i = 0;
	str_len = ft_strlen((char *)str);
	new_str = (char *)malloc(str_len + 1);
	if (!new_str)
		return (NULL);
	while (str[i])
	{
		new_str[i] = str[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
